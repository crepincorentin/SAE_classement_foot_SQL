    <h1>Présentation</h1>
    <div class="align2">
    <p>La première league est championnat de football d'Angleterre, c'est la plus importante compétition de football du pays. 
        Lancée en 1888 par la Football Association sous le nom de « Football League », la compétition laisse place 
        en 1992 à la Premier League, transformée en Barclays Premier League de 2004 à 2016 avant de reprendre son appellation précédente.
        La compétition se déroule annuellement, du mois d'août au mois de mai suivant, sous forme d'un championnat 
        mettant aux prises vingt clubs professionnels, actionnaires de la Premier League, qui disputent chacun 38 matchs. 
        À la fin de la saison, le premier est sacré champion, les suivants sont qualifiés pour les compétitions européennes 
        organisées par l'UEFA tandis que les trois équipes totalisant le plus faible nombre de points sont reléguées en EFL Championship, devenu l'échelon inférieur.
        Ce championnat est l'un des plus prestigieux au monde et le plus populaire en termes de téléspectateurs, 
        estimés à plus d'un milliard en 2007. Il est réputé pour être l'un des plus exigeants physiquement pour les 
        joueurs, en raison du calendrier dense malgré le passage du championnat de 22 à 20 clubs en 1995, et de l'engagement
        physique traditionnel du football anglais. La Premier League se place par ailleurs au premier rang européen des championnats 
        au coefficient UEFA de 2008 à 2012, après l'avoir déjà été de 1968 à 1975 et en 1985.
        Depuis 1888, 24 clubs ont remporté le championnat : les plus titrés sont Manchester United (20 titres, sept de 
        Football League et treize de Premier League), Liverpool (19 titres, dix-huit de Football League et un de Premier League) 
        et Arsenal (13 titres, dix de Football League et trois de Premier League).
    </div>
        <ul>
            <li class="sport"><u>Règles</u></li>
            La saison 2019-2020 de la Premier League est la 121e édition du championnat d'Angleterre de football et la 28e sous 
            l'appellation Premier League. Le plus haut niveau du football professionnel anglais, organisé par la Football Association Premier 
            League, oppose cette saison vingt clubs en une série de trente-huit rencontres jouées entre le 9 août 2019 et le 17 mai 2020.

            Lors de cette saison, le champion Manchester City défend son titre face à dix-neuf autres équipes dont trois promus de deuxième division.

            Cinq places qualificatives pour les compétitions européennes sont attribuées par le biais du championnat : quatre places en 
            Ligue des champions, et une en Ligue Europa. Les deux autres places européennes sont celles du vainqueur de la Coupe d'Angleterre 
            et de la Coupe de la Ligue qui sont qualificatives pour la Ligue Europa. Les trois derniers du championnat sont relégués en deuxième 
            division et sont remplacés par les trois promus de cette même division pour l'édition suivante.

            Cette saison est par ailleurs marquée par l'introduction d'une trêve de dix jours durant le mois de février 20201, ainsi 
            que de l'assistance vidéo à l'arbitrage pour tous les matchs du championnat2.

            La compétition est suspendue à partir du 13 mars 2020 en raison de la pandémie de Covid-19 qui affecte alors le Royaume-Uni, 
            puis reprend le mercredi 17 juin avec un nouveau calendrier reportant la fin de saison au 26 juillet.

            Le Liverpool Football Club est sacré champion d'Angleterre pour la 19e fois, trente ans après son dernier titre, à l'issue de la 31e journée.

            <li class="sport"><u>Équipes participantes</u></li>
            Un total de vingt équipes participent au championnat, les dix-sept maintenus de la saison précédente, auxquelles 
            s'ajoutent trois promus de deuxième division : Norwich City, Sheffield United et Aston Villa (vainqueur des barrages).

            Ces trois clubs remplacent les relégués Cardiff City, Fulham FC et Huddersfield Town.

            Parmi les 20 clubs, six n'ont jamais été relégués depuis la fondation de la Premier League en 1992 : Arsenal FC, 
            Chelsea FC, Everton FC, Liverpool, Manchester United et Tottenham Hotspur. En outre, Manchester City est le seul à 
            être présent depuis les années 2000 (promu en 2002).

